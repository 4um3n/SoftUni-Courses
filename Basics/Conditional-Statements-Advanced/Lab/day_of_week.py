num = int(input())

if num == 1:
    print(f"Monday")
elif num == 2:
    print(f"Tuesday")
elif num == 3:
    print(f"Wednesday")
elif num == 4:
    print(f"Thursday")
elif num == 5:
    print(f"Friday")
elif num == 6:
    print(f"Saturday")
elif num == 7:
    print(f"Sunday")
else:
    print(f"Error")
